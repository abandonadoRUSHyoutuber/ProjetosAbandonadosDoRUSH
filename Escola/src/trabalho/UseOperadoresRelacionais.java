package trabalho;

public class UseOperadoresRelacionais {

	public static void main(String[] args) {
		int x = 20;
		int y = 15;
		
		System.out.println(x > y);
		System.out.println(x >= y);
		System.out.println(x < y);
		System.out.println(x <= y);
		System.out.println(x == y);
		System.out.println(x != y);
	}
}
